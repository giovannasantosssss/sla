<p class="texto1"> 
    <br>
Raul Santos Seixas (Salvador, 28 de junho de 1945 — São Paulo, 21 de agosto de 1989) foi um <br>
cantor, compositor, produtor e multi-instrumentista brasileiro, frequentemente considerado um dos <br>
pioneiros do rock brasileiro. Também foi produtor musical da CBS, durante sua estadia na cidade do <br>
Rio de Janeiro e, por vezes, é chamado de Pai do Rock Brasileiro e Maluco Beleza. Sua obra musical é <br>
composta por dezessete discos lançados durante 26 anos de carreira. Seu estilo musical é <br>
tradicionalmente classificado como rock e baião, e de fato conseguiu unir ambos os gêneros em <br>
músicas como Let me Sing, Let me Sing. Seu álbum de estreia, Raulzito e os Panteras (1968) foi <br>
produzido quando integrava o grupo Raulzito e os Panteras, mas só ganhou notoriedade crítica e de <br>
público com músicas como Ouro de Tolo, Mosca na Sopa e Metamorfose Ambulante, do álbum Krig-ha, <br>
Bandolo!, de 1973. Raul Seixas tinha um estilo musical que era chamado de "contestador e místico". <br>
Isso se deve aos ideais que defendia, como a Sociedade Alternativa apresentada no álbum Gita, <br>
lançado em 1974, influenciado por figuras como o ocultista britânico Aleister Crowley. <br>
<br>
<p class="texto2"> 
Cético e agnóstico, Raul se interessava por filosofia (principalmente metafísica e ontologia), <br>
psicologia, história, literatura e latim. Algumas ideias dessas correntes foram muito aproveitadas em <br>
sua obra, que possuía uma recepção boa ou de curiosidade por conta disso. Foi um dos artistas mais <br>
aplaudidos na década do seu surgimento. Nos anos 1980, obteve declínio mas continuou produzindo <br>
álbuns que venderam bem, como Raul Seixas (1983), Uah-Bap-Lu-Bap-Lah-Béin-Bum! (1987) e A <br>
Panela do Diabo (1989), esse último em parceria com o também baiano e amigo Marcelo Nova. Sua <br>
obra musical tem aumentado continuamente de tamanho, na medida em que seus discos <br>
(principalmente álbuns póstumos) continuam a ser vendidos, tornando-o um símbolo do rock do país e <br>
um dos artistas mais cultuados e queridos entre os fãs nos últimos anos. Em outubro de 2008, a revista <br>
Rolling Stone promoveu a Lista dos Cem Maiores Artistas da Música Brasileira, cujo resultado colocou <br>
Raul Seixas na 19ª posição, superando nomes como Milton Nascimento, Maria Bethânia, Heitor Villa-Lobos <br>
e outros. No ano anterior, a mesma revista promoveu a Lista dos Cem Maiores Discos da <br>
Música Brasileira, onde dois de seus álbuns apareceram: Krig-ha, Bandolo!, na 12ª posição e Novo <br>
Aeon, na 53ª posição. <br>
